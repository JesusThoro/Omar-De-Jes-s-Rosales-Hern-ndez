// Ejercicio 7: Agregar y eliminar elementos en arrays
const colores = ["rojo", "azul", "verde"];

console.log("Array original:", colores);

// Agregar un elemento
colores.push("amarillo");
console.log("Después de agregar un elemento:", colores);

// Eliminar el último elemento
colores.pop();
console.log("Después de eliminar el último elemento:", colores);
